package Basics;

import java.util.Scanner;

public class Quadratic {

    public static void main(String[] args) {
int m=7;

      for (int i=1;i<=7;i++){
          for (int j=m;j>=1;j--){
              System.out.print(" "+j);
          }

          for (int k=2;k<=m;k++){
              System.out.print(" "+k);
          }
          m--;
          System.out.println("");


      }



    }
}
